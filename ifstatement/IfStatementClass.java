package ifstatement;

public class IfStatementClass {

	public static void main(String[] args) {
		// if syntax- if(condition) { }
		int a = 78;
		if (a > 89) {
			System.out.println("it is true");

		}
		// -----------------------------------------
		if (a < 79) {
			System.out.println("it is true");
		}
		// ---------------------------------------------
		boolean f1 = false;
		if (f1) {
			System.out.println("saurabh");

		}
		// ----------------------------------------
		boolean f2 = true;
		if (f2) {
			System.out.println("saurabh");
		}
		// --------------------------------------------
		if (true) {
			System.out.println("saurabh");

		}
		// ---------------------------------------------
		if (false) {
			System.out.println("saurabh");
		}
		// ------------------------------------------
		// If statement is used to run one condition only. It gives result on boolean condition
	}

}
