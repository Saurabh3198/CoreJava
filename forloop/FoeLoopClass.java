package forloop;

public class FoeLoopClass {

	public static void main(String[] args) {
		// for loop syntax-
		// for( initial condition; condition; incremental/ decremental)
		//    {
		//              }
		// if we write direct true or false in cindiotion for loop, it gives the compile error
		
		
		
		System.out.println("Before code");
		
		
		for(int i=0; i<5;i++) {
			
			System.out.println("saurabh");
			System.out.println("saurabh" +i);
		}
		//----------------------------------------------------------
		
		for (int i=5; i>=0; i--) {
			System.out.println("Soham");
		}
		//-------------------------------------------------------

		
		
		
		System.out.println("after code");

	}

}
